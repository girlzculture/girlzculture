import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";

const roots = ["src/app", "src/components", "src/lib", "netlify/functions"];
const files = roots
  .flatMap((root) => walk(root))
  .filter((file) => /\.(?:ts|tsx)$/.test(file));
const values = new Set();
const ignore =
  /^(?:[a-z0-9_-]+(?:\s+[a-z0-9_:\/[\].%-]+){2,}|GET|POST|PATCH|PUT|DELETE|USD|US|ltr|rtl|true|false)$/i;
const technical =
  /(?:^\/|https?:|@\/|\.tsx?$|\.sql$|bg-|text-|border-|rounded-|grid-|flex-|px-|py-|sm:|md:|lg:|xl:|[{}<>]|\$\{|^[a-z0-9_.-]+$)/i;

for (const file of files) {
  const source = fs.readFileSync(file, "utf8");
  const patterns = [
    ...(file.endsWith(".tsx")
      ? [
          />\s*([^<>{}\n][^<>{}]*?)\s*</g,
          /(?:placeholder|aria-label|title)=(?:"([^"]+)"|'([^']+)')/g,
        ]
      : []),
    /(?:setMessage|throw new Error|errorResponse)\(\s*(?:"([^"]+)"|'([^']+)'|`([^`$]+)`)/g,
  ];
  for (const pattern of patterns) {
    for (const match of source.matchAll(pattern)) {
      const text = String(match[1] || match[2] || match[3] || "")
        .replace(/\s+/g, " ")
        .trim();
      if (isCustomerText(text)) values.add(text);
    }
  }
}

const entries = [...values]
  .sort((a, b) => a.localeCompare(b))
  .map((source) => {
    const hash = crypto
      .createHash("sha1")
      .update(source)
      .digest("hex")
      .slice(0, 12);
    const impact = /(password|login|sign up|security|verification|identity|delete account)/i.test(
      source,
    )
      ? "security"
      : /(booking|appointment|cancel|refund|deposit|payment|price|subscription)/i.test(
            source,
          )
        ? "booking"
        : /(safety|complaint|moderation|report)/i.test(source)
          ? "safety"
          : "standard";
    return [`source.${hash}`, { source, impact }];
  });
const output = `// Generated by scripts/generate-interface-source-registry.mjs.\n// Do not hand-edit; regenerate after customer-facing copy changes.\nexport const GENERATED_SOURCE_MESSAGES = ${JSON.stringify(Object.fromEntries(entries), null, 2)} as const;\nexport type GeneratedSourceMessageKey = keyof typeof GENERATED_SOURCE_MESSAGES;\n`;
fs.writeFileSync("src/i18n/generated-source-messages.ts", output);
console.log(
  `Generated ${entries.length} interface source messages from ${files.length} TypeScript files.`,
);

function isCustomerText(text) {
  if (
    text.length < 2 ||
    text.length > 12000 ||
    ignore.test(text) ||
    technical.test(text)
  )
    return false;
  if (!/\p{L}/u.test(text)) return false;
  if (
    !/^[\p{L}\p{N}"'“‘(]/u.test(text) ||
    /(?:&&|\|\||===|!==|;|=>|\breturn\b|\bcase\b|\bconst\b|\btype\b|\bRecord\b|React\.)/.test(
      text,
    )
  )
    return false;
  if (/^[-+*=:,.()\[\]|]+$/.test(text)) return false;
  return /\s|[!?.,:]/.test(text);
}

function walk(root) {
  if (!fs.existsSync(root)) return [];
  return fs.readdirSync(root, { withFileTypes: true }).flatMap((entry) => {
    const target = path.join(root, entry.name);
    return entry.isDirectory() ? walk(target) : [target];
  });
}
